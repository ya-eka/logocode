let grade = 1;

switch (grade) {
    case 5:
        console.log('Отлично');
        break;
    case 4:
        console.log('Хорошо');
        break;
    case 3:
        console.log('Удовлетворительно');
        break;
    case 2:
        console.log('Неудовлетворительно');
        break;
    default:
        console.log('Неизвестно');
}
